    private void btn_uye_ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_uye_ekleActionPerformed
        
        try {
            
            String uye_kadi=txt_kadi.getText();
            String parola=txt_sifre.getText();
            String email=txt_email.getText();
            String unvan=String.valueOf(c_unvan.getSelectedItem());
            
            if (uye_kadi.equals("") || parola.equals("") || email.equals("")) {

                JOptionPane.showMessageDialog(this, "Lütfen Boş Alanları Doldurunuz!");

            } else {
                
                Class.forName("com.mysql.jdbc.Driver");
 
                Connection con = null;
                Statement st = null;
                ResultSet rs = null;
                
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurant", "root", "");
                st = con.createStatement();
                
                String sql = "INSERT INTO uyeler (kadi,sifre,email,unvan)" 
                        + " VALUES ('"+uye_kadi+"', '"+parola+"', '"+email+"', '"+unvan+"')";

                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Sayın "+ uye_kadi + " Kayıt İşleminiz Gerçekleşmiştir.");
             
            }
            
        } catch (ClassNotFoundException ex) {
           JOptionPane.showMessageDialog(null,"Hata: " + ex.toString());

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Veritabanına bağlantı sağlanamadı! " + ex.toString());
        }
        
    }//GEN-LAST:event_btn_uye_ekleActionPerformed

