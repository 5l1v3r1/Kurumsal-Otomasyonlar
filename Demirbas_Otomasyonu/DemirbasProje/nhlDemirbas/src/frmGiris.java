    private void btnGirisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGirisActionPerformed
        
        int sayi = 0;
        String kulTipi = null;
        try{
            String sSql = "select Grup from kullanicitanimlari where KullaniciAdi = ? and Sifre = ?";
            pst=con.prepareStatement(sSql);
            pst.setString(1,txtKullanici.getText());
            pst.setString(2, txtSifre.getText());
            rs = pst.executeQuery();
            
            while(rs.next())
                {
                   kulTipi  = rs.getObject(1).toString();
                   sayi++;
                }
            
            if ((sayi > 0) && ("Admin".equals(kulTipi))) {
                KullaniciYetki = "Admin";
                setVisible(false);
                new AnaMenu().setVisible(true);
            }
            
            if ((sayi > 0) && ("User".equals(kulTipi))) {
                KullaniciYetki ="User";
                setVisible(false);
                new frmKullaniciMenu().setVisible(true);
            }
            
            if(sayi == 0)
            { 
                JOptionPane.showMessageDialog(null,"Yanlış Kullanıcı Adı yada Şifre");
            }
        
           
        }catch(SQLException  e){
            JOptionPane.showMessageDialog(null,e);
            
        }
        
    }//GEN-LAST:event_btnGirisActionPerformed

