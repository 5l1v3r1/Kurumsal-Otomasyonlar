    public void tablodoldur() throws SQLException {


        String baslik[] = new String[]{"Adı", "Soyadı", "TC","Kayıt Tarihi","Veli Adı","Veli Soyadı","Veli Telefon"};;

        try {
            baglan();

            String sorgu = "select * from ogrenci";

            stmt = conn.createStatement();
             rs = stmt.executeQuery(sorgu);

           
            rs.last();
            int kayitsayisi = rs.getRow();
            rs.beforeFirst();


            Object data[][] = new Object[kayitsayisi][];
            int i = 0;

            while (rs.next()) {

                data[i] = new Object[]{
                    rs.getString("oadi"),
                    rs.getString("osoyadi"),
                    rs.getString("otc"),
                    rs.getString("ktarih"),
                    rs.getString("vadi"),
                    rs.getString("vsoyadi"),
                    rs.getString("telefon")
                };
                i++;
            }
            conn.close();
            jTable1.setModel(new DefaultTableModel(data, baslik));
        } catch (Exception ex) {
        }

    }

